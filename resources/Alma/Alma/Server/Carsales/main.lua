Alma:AddEventHandler("Luxury", "PayNewVehicle", function(modelVeh)
    local source = source;
    local xPlayer = ESX.GetPlayerFromId(source);

    for l,k in pairs(Concess.vehicles) do
        for i,v in pairs(k.vehicles) do
            if modelVeh == v.hash then
                if xPlayer.getAccount('bank').money >= v.price then
					SetPlayerRoutingBucket(source, 0)
                    xPlayer.removeAccountMoney('bank', v.price)
                    xPlayer.showNotification("Payée par compte en banque:\n~r~- "..ESX.Math.GroupDigits(v.price).."$")
                    AddVehicleToPlayer(v.hash, xPlayer)
                    Helper:showAdvancedNotification(source, "Concessionnaire", "Luxury Motors", "Bonjour, ~g~merci de votre achat chez nous~s~, voici les clés de votre ~g~"..v.name.."~s~, bonne route !", "CHAR_MALC", 1)
                else
                    if xPlayer.getAccount('money').money >= v.price then
						SetPlayerRoutingBucket(source, 0)
                        xPlayer.removeAccountMoney('money', v.price)
                        xPlayer.showNotification("Payée en liquide:\n~r~-"..ESX.Math.GroupDigits(v.price).."$")
                        AddVehicleToPlayer(v.hash, xPlayer)
                        Helper:showAdvancedNotification(source, "Concessionnaire", "Luxury Motors", "Bonjour, ~g~merci de votre achat chez nous~s~, voici les clés de votre ~g~"..v.name.."~s~, bonne route !", "CHAR_MALC", 1)
                    else
                        xPlayer.showNotification("Vous n'avez pas assez pour acheter "..v.name.." ~r~(requis "..v.price..")~s~")
                    end
                end
            end
        end
    end
end)