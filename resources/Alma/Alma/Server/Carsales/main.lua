function AddVehicleToPlayer(model, xPlayer)
    local veh_plate = CreateRandomPlateText()
    print(veh_plate)
    MySQL.Async.execute("INSERT INTO owned_vehicles (owner, plate, vehicle, type, state, boutique, selling) VALUES(@owner, @plate, @vehicle, 'car', 0, 0, 0)", {
        ["@owner"] = xPlayer.identifier,
        ["@plate"] = veh_plate,
        ["@vehicle"] = json.encode({ model = GetHashKey(model), plate = veh_plate })
    }, function(owned_vehicles)
        if owned_vehicles then
            MySQL.Async.execute("INSERT INTO open_car (owner, plate, NB) VALUES(@owner, @plate, 0)", {
                ["@owner"] = xPlayer.identifier,
                ["@plate"] = veh_plate
            }, function(open_car)
                if open_car then
                    SetEntityCoords(GetPlayerPed(xPlayer.source), -763.220703125, -250.23156738281, 37.09511184692)
                    local vehicleGived = CreateVehicle(GetHashKey(model), -763.220703125, -250.23156738281, 37.095111846924, 108.0, true, false)
                    TaskWarpPedIntoVehicle(GetPlayerPed(xPlayer.source), vehicleGived, -1)
                    SetVehicleNumberPlateText(vehicleGived, veh_plate)
                end
            end)
        end
    end)
end

